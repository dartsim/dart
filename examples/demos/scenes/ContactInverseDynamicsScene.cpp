// Ported from examples/contact_inverse_dynamics: a kinematic squat cycle for
// fullbody1.skel (60 Catmull-Rom keyframes generated by hierarchical
// whole-body IK) solved every frame for contact-consistent joint torques via
// dart::dynamics::ContactInverseDynamics, with force arrows at the four foot
// contacts and ImGui plots of the vertical GRF and three leg torques.
//
// Deviations from the original: fullbody1.skel is authored Y-up; reoriented
// to the host's Z-up convention via ZUp.hpp, like every other B3
// fullbody1.skel scene. The squat depth is applied along the pelvis target's
// Z translation (was Y in the original) because ZUp.hpp maps the source
// world's +Y (up) onto +Z; the up direction used for contact normals is
// derived from gravity (already rotated by ZUp.hpp), so it needs no such
// change. The world is never physically stepped, exactly as in the original
// (all motion is kinematic, driven by an internal clock); since this host
// always calls World::step() for the Step button, --cycle-scenes, and
// --headless capture, this scene's preStep/postStep hooks are left empty and
// the entire per-frame update (advance the clock, evaluate the spline,
// re-solve contacts, update the arrows) runs at the top of renderPanel
// instead -- called once per rendered frame regardless of pause state, the
// same cadence the original's customPreRefresh had (see TinkertoyScene.cpp's
// file comment for the same pattern) -- so the kinematic pose this renders is
// always correct by the time a frame is actually drawn (including the few
// frames rendered before a --headless screenshot), even though raw
// world-stepping during Step/cycle-scenes/headless has no effect on it either
// way. The original's atomic-flag-deferred keyframe regeneration existed to
// keep an ImGui draw-thread callback from touching the skeleton directly;
// this host is single-threaded end to end (no separate draw thread), so
// regeneration runs synchronously from the panel callback instead. The
// original's --frames/--screenshot console-capture mode and --gui-scale flag
// are dropped in favor of the host's generic --cycle-scenes/--headless/
// --gui-scale equivalents.

