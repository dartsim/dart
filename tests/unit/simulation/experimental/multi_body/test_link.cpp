TEST(Link, MultipleLinkChain)
{
  dse::World world;
  auto robot = world.addMultiBody("robot");

  auto base = robot.addLink("base");
  auto link1
      = robot.addLink("link1", {.parentLink = base, .jointName = "joint1"});
  auto link2
      = robot.addLink("link2", {.parentLink = link1, .jointName = "joint2"});
  auto link3
      = robot.addLink("link3", {.parentLink = link2, .jointName = "joint3"});

  EXPECT_EQ(robot.getLinkCount(), 4u);

  EXPECT_FALSE(base.getParentJoint().isValid());
  EXPECT_TRUE(link1.getParentJoint().isValid());
  EXPECT_TRUE(link2.getParentJoint().isValid());
  EXPECT_TRUE(link3.getParentJoint().isValid());

  EXPECT_EQ(link1.getParentJoint().getParentLink().getName(), "base");
  EXPECT_EQ(link2.getParentJoint().getParentLink().getName(), "link1");
  EXPECT_EQ(link3.getParentJoint().getParentLink().getName(), "link2");
}

TEST(Link, BranchingTree)
{
  dse::World world;
  auto robot = world.addMultiBody("humanoid");

  auto torso = robot.addLink("torso");
  auto leftArm
      = robot.addLink("left_arm", {.parentLink = torso, .jointName = "l_sho"});
  auto rightArm
      = robot.addLink("right_arm", {.parentLink = torso, .jointName = "r_sho"});
  auto head = robot.addLink("head", {.parentLink = torso, .jointName = "neck"});

  EXPECT_EQ(robot.getLinkCount(), 4u);
  EXPECT_EQ(robot.getJointCount(), 3u);

  EXPECT_EQ(leftArm.getParentJoint().getParentLink().getName(), "torso");
  EXPECT_EQ(rightArm.getParentJoint().getParentLink().getName(), "torso");
  EXPECT_EQ(head.getParentJoint().getParentLink().getName(), "torso");
}

