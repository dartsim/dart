TEST(Link, RootLinkHasInvalidParentJoint)
{
  dse::World world;
  auto robot = world.addMultiBody("robot");
  auto base = robot.addLink("base");

  auto parentJoint = base.getParentJoint();
  EXPECT_FALSE(parentJoint.isValid());
}

TEST(Link, NonRootLinkHasValidParentJoint)
{
  dse::World world;
  auto robot = world.addMultiBody("robot");
  auto base = robot.addLink("base");
  auto link1
      = robot.addLink("link1", {.parentLink = base, .jointName = "joint1"});

  auto parentJoint = link1.getParentJoint();
  EXPECT_TRUE(parentJoint.isValid());
  EXPECT_EQ(parentJoint.getName(), "joint1");
}

TEST(Link, GetParentJointType)
{
  dse::World world;
  auto robot = world.addMultiBody("robot");
  auto base = robot.addLink("base");
  auto link1 = robot.addLink(
      "link1",
      {.parentLink = base,
       .jointName = "revolute_joint",
       .jointType = dse::comps::JointType::Revolute});
  auto link2 = robot.addLink(
      "link2",
      {.parentLink = link1,
       .jointName = "prismatic_joint",
       .jointType = dse::comps::JointType::Prismatic});

  EXPECT_EQ(link1.getParentJoint().getType(), dse::comps::JointType::Revolute);
  EXPECT_EQ(link2.getParentJoint().getType(), dse::comps::JointType::Prismatic);
}

